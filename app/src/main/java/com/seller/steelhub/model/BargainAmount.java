public class BargainAmount {

    private String size, quantity, amount, bargain_amount;

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public String getQuantity() {
        return quantity;
    }

    public void setQuantity(String quantity) {
        this.quantity = quantity;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getBargain_amount() {
        return bargain_amount;
    }

    public void setBargain_amount(String bargain_amount) {
        this.bargain_amount = bargain_amount;
    }
}
